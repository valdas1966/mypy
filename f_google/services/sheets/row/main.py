from f_google.services.sheets.cell.main import Cell


class Row:
    """
    ============================================================================
     Google Sheets Row.
    ============================================================================
    """

    def __init__(self, values: list[str]) -> None:
        """
        ====================================================================
         Init with a list of Cell Values.
        ====================================================================
        """
        self._cells = [Cell(value=v) for v in values]

    def __getitem__(self, col: int) -> Cell:
        """
        ====================================================================
         Return a Cell by Column Index (0-based).
        ====================================================================
        """
        return self._cells[col]

    def __len__(self) -> int:
        """
        ====================================================================
         Return the number of Columns.
        ====================================================================
        """
        return len(self._cells)

    def __iter__(self):
        """
        ====================================================================
         Iterate over Cells.
        ====================================================================
        """
        return iter(self._cells)
