class Cell:
    """
    ============================================================================
     Google Sheets Cell.
    ============================================================================
    """

    def __init__(self, value: str) -> None:
        """
        ====================================================================
         Init with a Cell Value.
        ====================================================================
        """
        self._value = value

    @property
    def value(self) -> str:
        """
        ====================================================================
         Return the Cell Value.
        ====================================================================
        """
        return self._value

    def __str__(self) -> str:
        return self._value
