from f_gui.layout.rect.rect import Rect


class FactoryRect:
    """
    ========================================================================
     Generator for LTWH objects.
    ========================================================================
    """

    @staticmethod
    def full() -> Rect:
        """
        ========================================================================
         Generate a full-size LTWH.
        ========================================================================
        """
        return Rect(top=0, left=0, width=100, height=100)
    
    @staticmethod
    def half() -> Rect:
        """
        ========================================================================
         Generate a half-size LTWH.
        ========================================================================
        """
        return Rect(top=25, left=25, width=50, height=50)
    
    @staticmethod
    def quarter() -> Rect:
        """
        ========================================================================
         Generate a quarter-size LTWH.
        ========================================================================
        """ 
        return Rect(top=37.5, left=37.5, width=25, height=25)
