from f_color.rgb import RGB


class HasColor:
    """
    ============================================================================
     Mixin for components that have a color.
    ============================================================================
    """

    def __init__(self, color: RGB = None) -> None:
        """
        ========================================================================
         Initialize the HasColor mixin.
        ========================================================================
        """
        self._color = color

    @property
    def color(self) -> RGB:
        """
        ========================================================================
         Get the color.
        ========================================================================
        """
        return self._color

    @color.setter
    def color(self, color: RGB) -> None:
        """
        ========================================================================
         Set the color.
        ========================================================================
        """
        self._color = color
