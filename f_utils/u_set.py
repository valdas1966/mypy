def get(s, item):
    """
    ===========================================================================
     Description: Return the Item from the Set (None if not exists).
    ===========================================================================
     Arguments:
    ---------------------------------------------------------------------------
        1. s : set 
        2. item : item
    ===========================================================================
     Return: item (None if not exists).
    ===========================================================================
    """
    for x in s:
        if item == x:
            return x
    return None
    

def generalize(s):
    """
    =======================================================================
     Description: If given only one value (not set), insert the value into
                    empty set and return the set (with on value).
    =======================================================================
     Arguments:
    -----------------------------------------------------------------------
        1. s : set or not.
    =======================================================================
     Return: set (Given Set or New Set with one given value).
    =======================================================================
    """
    if isinstance(s, set):
        return s
    return {s}

    
