
class Mask:
    """
    ============================================================================
     Utils-Class for masking a String.
    ============================================================================
    """

    @staticmethod
    def full(s: str,
             char_mask: str = '*') -> str:
        """
        ========================================================================
         Mask a String (except blank chars).
        ========================================================================
        """
        return ''.join(' ' if ch == ' ' else char_mask for ch in s)
