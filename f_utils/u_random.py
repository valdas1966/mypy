from random import randint

def get_random_int(a, b):
    """
    ===========================================================================
     Description: Return Random Number between a and b.
    ===========================================================================
     Arguments:
    ---------------------------------------------------------------------------
        1. a : int (Random Number From).
        2. b : int (Random Number To).
    ===========================================================================
     Return: int (Random Number between a and b).
    ===========================================================================
    """
    return randint(a,b)