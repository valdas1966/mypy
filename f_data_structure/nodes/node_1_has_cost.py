from f_data_structure.nodes.node_0_nameable import NodeNameable
from f_data_structure.mixins.has_cost import HasCost


class NodeHasCost(NodeNameable, HasCost):
    """
    ============================================================================
     Node with a Cost-Function.
    ============================================================================
     Methods:
    ----------------------------------------------------------------------------
        1. cost() -> int
           [*] Returns Object's Cost-Function Value.
    ============================================================================
     Magic Methods:
    ----------------------------------------------------------------------------
        # NameAble
    ----------------------------------------------------------------------------
        1. str() -> str
        2. repr() -> str
    ----------------------------------------------------------------------------
        # HasCost
    ----------------------------------------------------------------------------
        3. eq() -> bool
        4. ne() -> bool
        5. lt() -> bool
        6. le() -> bool
        7. gt() -> bool
        8. ge() -> bool
    ============================================================================
    """

    # NameAble
    name: str                     # Object's Name

    def __init__(self, name: str = None) -> None:
        """
        ========================================================================
         Initializes the Base-Classes.
        ========================================================================
        """
        NodeNameable.__init__(self, name)
        HasCost.__init__(self)
