import pandas as pd


class GenSeries:
    """
    ============================================================================
     Series Generator Class.
    ============================================================================
    """
    
    @staticmethod
    def five() -> pd.Series:
        """
        ========================================================================
        Generate a series with numbers 1 to 5.
        ========================================================================
        """
        vals = [1, 2, 3, 4, 5]
        return pd.Series(vals)

    @staticmethod
    def fibonacci(n: int) -> pd.Series:
        """
        ========================================================================
         Generate a series with the first n Fibonacci numbers.
        ------------------------------------------------------------------------
         Input:
        ------------------------------------------------------------------------
         n = 5
        ------------------------------------------------------------------------
         Output:
        ------------------------------------------------------------------------
         val
         0  0
         1  1
         2  1
         3  2
         4  3
        ========================================================================
        """
        name = 'val'
        data = [0, 1]
        for i in range(2, n):
            data.append(data[i-1] + data[i-2])
        return pd.Series(name=name, data=data)



