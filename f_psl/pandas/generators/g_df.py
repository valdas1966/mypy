
import pandas as pd


class GenDF:
    """
    ============================================================================
     DataFrame Generator Class.
    ============================================================================
    """

    @staticmethod
    def format_col() -> pd.DataFrame:
        """
        ========================================================================
         Format the column `col` to the nearest multiple.
        ========================================================================
        """
        x = [1, 2, 3, 4, 5]
        y = [1, 2, 3, 4, 5]
        data = {'x': x, 'y': y}
        return pd.DataFrame(data)
