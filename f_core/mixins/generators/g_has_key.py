from f_core.mixins.has.key import HasKey


class GenHasKey(HasKey):
    """
    ============================================================================
    
    ============================================================================
    """ 

    @staticmethod
    def one() -> HasKey[int]:
        """
        ========================================================================
         Return a HasKey with a key of 1.
        ========================================================================
        """
        return HasKey(key=1)

    @staticmethod
    def two() -> HasKey[int]:
        """
        ========================================================================
         Return a HasKey with a key of 2.
        ========================================================================
        """
        return HasKey(key=2)
