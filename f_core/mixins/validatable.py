
class Validatable:
    """
    ============================================================================
     Mixin-Class for Validatable Objects.
    ============================================================================
    """

    def __init__(self, is_valid: bool = None) -> None:
        """
        ========================================================================
         Init private Attributes.
        ========================================================================
        """
        self._is_valid = is_valid

    def __bool__(self) -> bool:
        """
        ========================================================================
         Return True if the Object is Valid.
        ========================================================================
        """
        return self._is_valid
