
class HasVerbose:
    """
    ============================================================================
     Mixin for Processes with Verbose-Attribute.
    ============================================================================
    """

    def __init__(self, verbose: bool = True) -> None:
        """
        ========================================================================
         Init private Attributes.
        ========================================================================
        """
        self._verbose = verbose

    @property
    def verbose(self) -> bool:
        """
        ========================================================================
         Get the Verbose-Attribute.
        ========================================================================
        """
        return self._verbose
