class FreeText:
    """
    ===========================================================================
     Description: Free Text for HTML with:
    ----------------------------------------------------------------------------
        1. Variable number of indents at the beginning.
        2. End-line character at the end of the text.
    ===========================================================================
     Example: '\t\tHello World!\n'
    ===========================================================================
     Attributes:
    ---------------------------------------------------------------------------
        1. self.text : str (Free Text with end-line character).
    ===========================================================================
    """
    
    def __init__(self, text, indents=0):
        """
        =======================================================================
         Description: Constructor
        =======================================================================
         Arguments:
        -----------------------------------------------------------------------
            1. text : str (Free Text).
            2. indents : int (Number of Indents).
        =======================================================================
        """
        self.text = '\t'*indents + text + '\n'

    def __str__(self):
        """
        =======================================================================
         Description: String Representation
        =======================================================================
         Return: str
        =======================================================================
        """
        return self.text

    def __eq__(self, other):
        """
        =======================================================================
         Description: Equal Operator (if string representations are equal).
        =======================================================================
         Arguments:
        -----------------------------------------------------------------------
            1. other : Free_Text
        =======================================================================
         Return: bool 
        =======================================================================
        """
        return str(self) == str(other)
