class HasH:
    """
    ============================================================================
     Mixin for Nodes with Heuristic-Values.
    ============================================================================
    """

    def __init__(self, h: int = None) -> None:
        """
        ========================================================================
         Init private Attributes.
        ========================================================================
        """
        self._h = h

    @property
    def h(self) -> int:
        """
        ========================================================================
         Return a Heuristic-Distance from the current Node to the Goal.
        ========================================================================
        """
        return self._h

    @h.setter
    def h(self, val: int) -> None:
        """
        ========================================================================
         Set a Heuristic-Distance from the current Node to the Goal.
        ========================================================================
        """
        self._h = val
