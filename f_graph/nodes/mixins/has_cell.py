from f_ds.grids.cell import Cell


class HasCell:
    """
    ============================================================================
     Mixin-Class for Nodes with Cell.
    ============================================================================
    """

    def __init__(self, cell: Cell) -> None:
        """
        ========================================================================
         Init private Attributes.
        ========================================================================
        """
        self._cell = cell

    @property
    def cell(self) -> Cell:
        """
        ========================================================================
         Return the Node's Cell.
        ========================================================================
        """
        return self._cell
