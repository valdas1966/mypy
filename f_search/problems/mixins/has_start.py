from f_search import State


class HasStart:
    """
    ============================================================================
     Mixin-Class for Problems with a Start-State.
    ============================================================================
    """

    def __init__(self, start: State) -> None:
        """
        ========================================================================
         Init private Attributes.
        ========================================================================
        """
        self._start = start

    @property
    def start(self) -> State:
        """
        ========================================================================
         Return the Start-State of the Problem.
        ========================================================================
        """
        return self._start