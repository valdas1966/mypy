from f_search.state import State


class HasGoal:
    """
    ============================================================================
     Mixin-Class for Problems with a Goal-State.
    ============================================================================
    """

    def __init__(self, goal: State) -> None:
        """
        ========================================================================
         Init private Attributes.
        ========================================================================
        """
        self._goal = goal   

    @property
    def goal(self) -> State:
        """
        ========================================================================
         Return the Goal-State of the Problem.
        ========================================================================
        """
        return self._goal
