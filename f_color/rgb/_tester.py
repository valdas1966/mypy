from f_color.rgb import RGB


def test_init() -> None:
    """
    ========================================================================
     Test the initialization of the RGB-class.
    ========================================================================
    """
    black_1 = RGB(r=0, g=0, b=0)
    black_2 = RGB(name='BLACK')
    assert black_1 == black_2


def test_to_tuple() -> None:
    """
    ========================================================================
     Test the to.tuple() method.
    ========================================================================
    """
    rgb = RGB(name='WHITE')
    assert rgb.to.tuple() == (1, 1, 1)
    assert rgb.to.tuple(to_int=True) == (255, 255, 255)


def test_to_hex() -> None:
    """
    ========================================================================
     Test the to.hex() method.
    ========================================================================
    """
    rgb = RGB(r=1.0, g=0.0, b=0.0)
    assert rgb.to.hex() == '#FF0000'


def test_to_argb() -> None:
    """
    ========================================================================
     Test the to.argb() method.
    ========================================================================
    """
    rgb = RGB(r=1.0, g=0.0, b=0.0)
    assert rgb.to.argb() == 'FFFF0000'


def test_str() -> None:
    """
    ========================================================================
     Test the __str__() method.
    ========================================================================
    """
    rgb = RGB(r=0.5, g=0.5, b=0.5)
    assert str(rgb) == '(0.5, 0.5, 0.5)'


def test_key() -> None:
    """
    ========================================================================
     Test the key property.
    ========================================================================
    """
    rgb = RGB(r=0.5, g=0.5, b=0.5)
    assert rgb.key == (0.5, 0.5, 0.5)


def test_eq() -> None:
    """
    ========================================================================
     Test the __eq__() method.
    ========================================================================
    """
    black = RGB(name='BLACK')
    assert black == RGB(r=0, g=0, b=0)


def test_lt() -> None:
    """
    ========================================================================
     Test the __lt__() method.
    ========================================================================
    """
    black = RGB(name='BLACK')
    white = RGB(name='WHITE')
    assert black < white
    assert not (white < black)


def test_from_ints() -> None:
    """
    ========================================================================
     Test the From.ints() method.
    ========================================================================
    """
    rgb = RGB.From.ints(r=255, g=0, b=0)
    assert rgb == RGB(r=1, g=0, b=0)


def test_from_hex() -> None:
    """
    ========================================================================
     Test the From.hex() method.
    ========================================================================
    """
    rgb = RGB.From.hex('#000000')
    assert rgb == RGB(r=0, g=0, b=0)


def test_gradient() -> None:
    """
    ========================================================================
     Test the Factory.gradient() method.
    ========================================================================
    """
    white = RGB(name='WHITE')
    black = RGB(name='BLACK')
    rgb = RGB.Factory.gradient(a=white, b=black, n=5)
    assert rgb[0] == RGB(r=1, g=1, b=1)
    assert rgb[2] == RGB(r=0.5, g=0.5, b=0.5)
    assert rgb[4] == RGB(r=0, g=0, b=0)
