from math import gcd


def lcm(a, b):
    """
    ============================================================================
     Description: Return LCM (Least Common Multiple) of two given integers.
    ============================================================================
     Arguments:
    ----------------------------------------------------------------------------
        1. a : int
        2. b : int
    ============================================================================
     Return: int in range [max(a,b), a*b]
    ============================================================================
    """
    assert type(a) == int
    assert type(b) == int
    assert a >= 1
    assert b >= 1
    return a*b // gcd(a,b)


