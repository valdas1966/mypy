
def factors(n):
    """
    ============================================================================
     Description: Return Set of Factors of the given Integer.
    ----------------------------------------------------------------------------
        1. Factors(n) = {x for x*y=n, when x,y,n are positive int}
    ============================================================================
     Arguments:
    ----------------------------------------------------------------------------
        1. n : positive int
    ============================================================================
     Return: set of int (Set of Factors of a given positive number N).
    ============================================================================
    """
    assert type(n) == int
    assert n > 0
    s = {1, n}
    for x in range(2, int(n**0.5)+1):
        if n % x == 0:
            y = n // x
            s = s.union({x, y})
    return s


def common_factors(a, b):
    """
    ============================================================================
     Description: Return sorted List of Common Factors of 2 given Integers.
    ============================================================================
     Arguments:
    ----------------------------------------------------------------------------
        1. a : int
        2. b : int
    ============================================================================
     Return: list of int (Sorted List of Common Factors).
    ============================================================================
    """
    factors_a = set(factors(a))
    factors_b = set(factors(b))
    common = factors_a.intersection(factors_b)
    return sorted([x for x in common])


def gcf(a, b):
    """
    ============================================================================
     Description: Return the Greatest Common Factor of two given integers.
    ============================================================================
     Arguments:
    ----------------------------------------------------------------------------
        1. a : int
        2. b : int
    ============================================================================
     Return: int (GCF of two given Integers).
    ============================================================================
    """
    common = common_factors(a, b)
    return max(common)


def is_prime(n):
    """
    ============================================================================
     Description: Return True if the given number is a Prime Number.
    ============================================================================
     Arguments:
    ----------------------------------------------------------------------------
        1. n : int
    ============================================================================
     Result: bool
    ============================================================================
    """
    assert type(n) == int
    if n <= 0:
        return False
    return len(factors(n) == 2)
