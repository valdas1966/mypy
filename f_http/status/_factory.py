from f_http.status.main import Status


class Factory:
    """
    ============================================================================
     Factory for creating Status instances.
    ============================================================================
    """

    @staticmethod
    def ok() -> Status:
        """
        ========================================================================
         Returns a valid status (200 OK).
        ========================================================================
        """
        return Status(code=200)

    @staticmethod
    def not_found() -> Status:
        """
        ========================================================================
         Returns a not found status (404).
        ========================================================================
        """
        return Status(code=404)

    @staticmethod
    def bad_request() -> Status:
        """
        ========================================================================
         Returns a bad request status (400).
        ========================================================================
        """
        return Status(code=400)

    @staticmethod
    def unknown() -> Status:
        """
        ========================================================================
         Returns an unknown status (code=None).
        ========================================================================
        """
        return Status(code=None)
