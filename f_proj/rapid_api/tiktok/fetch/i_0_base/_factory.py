from f_http import Status
from typing import Any


class Factory:
    """
    ============================================================================
     Factory for FetchBase Test Objects.
    ============================================================================
    """

    @staticmethod
    def row() -> dict[str, Any]:
        """
        ====================================================================
         Sample row before stamping.
        ====================================================================
        """
        return {'id_user': '123', 'nick': 'test_user'}

    @staticmethod
    def anchor() -> tuple[str, str]:
        """
        ====================================================================
         Sample anchor tuple.
        ====================================================================
        """
        return ('id_user', '123')

    @staticmethod
    def status_error() -> Status:
        """
        ====================================================================
         A 404 error status.
        ====================================================================
        """
        return Status(code=404)

    @staticmethod
    def msg() -> str:
        """
        ====================================================================
         Sample error message.
        ====================================================================
        """
        return 'KeyError: user'
