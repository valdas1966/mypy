
class HasEager:
    """
    ========================================================================
     Mixin for Objects that have an Eagerness.
    ========================================================================
    """
    def __init__(self, is_eager: bool = False) -> None:
        """
        ====================================================================
         Init private Attributes.
        ====================================================================
        """
        self._is_eager = is_eager

    @property
    def is_eager(self) -> bool:
        """
        ====================================================================
         Get the Eagerness of the Object.
        ====================================================================
        """
        return self._is_eager
