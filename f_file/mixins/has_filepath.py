
class HasFilePath:
    """
    ============================================================================
     Mixin for classes that have a file path.
    ============================================================================
    """
    
    def __init__(self, filepath: str) -> None:
        """
        ========================================================================
         Init private Attributes.
        ========================================================================
        """
        self._filepath = filepath

    @property
    def filepath(self) -> str:
        """
        ========================================================================
         Getter for filepath.
        ========================================================================
        """
        return self._filepath
