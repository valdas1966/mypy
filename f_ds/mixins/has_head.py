from typing import Generic, TypeVar


T = TypeVar('T')


class HasHead(Generic[T]):
    """
    ============================================================================
     Mixin for Objects with a Head property.
    ============================================================================
    """

    def __init__(self) -> None:
        """
        ========================================================================
         Initialize the Head property.
        ========================================================================
        """
        self._head: T = None

    @property
    def head(self) -> T:
        """
        ========================================================================
         Get the head property.
        ========================================================================
        """
        return self._head

    @head.setter
    def head(self, value: T) -> None:
        """
        ========================================================================
         Set the head property.
        ========================================================================
        """
        self._head = value
