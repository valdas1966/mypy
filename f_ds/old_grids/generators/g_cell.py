from f_ds.old_grids.old_cell import Cell


class GenCell:
    """
    ============================================================================
     Generate Cells.
    ============================================================================
    """

    @staticmethod
    def zero() -> Cell:
        """
        ========================================================================
         Return a Cell with value 0.
        ========================================================================
        """
        return Cell(row=0)
    
    @staticmethod
    def one() -> Cell:
        """
        ========================================================================
         Return a Cell with value 1.
        ========================================================================
        """
        return Cell(row=1)
