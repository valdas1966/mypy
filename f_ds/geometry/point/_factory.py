from f_ds.geometry.point.main import Point


class Factory:
    """
    ========================================================================
     Factory for Point objects.
    ========================================================================
    """

    @staticmethod
    def origin() -> Point:
        """
        ========================================================================
         Generate the top-left origin Point (0, 0).
        ========================================================================
        """
        return Point(x=0, y=0)

    @staticmethod
    def center() -> Point:
        """
        ========================================================================
         Generate the centered Point (50, 50).
        ========================================================================
        """
        return Point(x=50, y=50)

    @staticmethod
    def end() -> Point:
        """
        ========================================================================
         Generate the bottom-right Point (100, 100).
        ========================================================================
        """
        return Point(x=100, y=100)
