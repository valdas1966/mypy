from f_ds.geometry.pointxy.main import PointXY


class Factory:
    """
    ========================================================================
     Factory for PointXY objects.
    ========================================================================
    """

    @staticmethod
    def zero() -> PointXY:
        """
        ========================================================================
         Generate the zero PointXY (0, 0).
        ========================================================================
        """
        return PointXY(x=0, y=0)
