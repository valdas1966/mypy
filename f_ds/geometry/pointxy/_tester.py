from f_ds.geometry.pointxy.main import PointXY


def test_x() -> None:
    """
    ========================================================================
     Test the x property.
    ========================================================================
    """
    p = PointXY(x=30, y=70)
    assert p.x == 30


def test_y() -> None:
    """
    ========================================================================
     Test the y property.
    ========================================================================
    """
    p = PointXY(x=30, y=70)
    assert p.y == 70


def test_to_tuple() -> None:
    """
    ========================================================================
     Test the to_tuple() method.
    ========================================================================
    """
    p = PointXY(x=30, y=70)
    assert p.to_tuple() == (30, 70)


def test_eq() -> None:
    """
    ========================================================================
     Test equality via the (x, y) key.
    ========================================================================
    """
    a = PointXY(x=10, y=20)
    b = PointXY(x=10, y=20)
    assert a == b


def test_hash() -> None:
    """
    ========================================================================
     Test that equal Points hash equally (usable as set/dict keys).
    ========================================================================
    """
    a = PointXY(x=10, y=20)
    b = PointXY(x=10, y=20)
    assert len({a, b}) == 1


def test_str() -> None:
    """
    ========================================================================
     Test the string representation.
    ========================================================================
    """
    assert str(PointXY(x=30, y=70)) == '(30, 70)'
