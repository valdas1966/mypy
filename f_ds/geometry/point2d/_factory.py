from f_ds.geometry.point2d.main import Point2D


class Factory:
    """
    ========================================================================
     Factory for Point2D objects.
    ========================================================================
    """

    @staticmethod
    def zero() -> Point2D:
        """
        ========================================================================
         Generate the zero Point2D (0, 0).
        ========================================================================
        """
        return Point2D(row=0, col=0)
