from f_ds.grids.grid.base.main import GridBase


class FactoryGridBase:
    """
    ============================================================================
     Factory for GridBase.
    ============================================================================
    """

    @staticmethod
    def grid_3x3() -> GridBase:
        """
        ========================================================================
         Create a 3x3 grid.
        ========================================================================
        """
        return GridBase(rows=3)
